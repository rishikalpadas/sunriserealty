    <script>
        const gallery = document.getElementById('gallery');
        const lightbox = document.getElementById('lightbox');
        const lightboxImg = document.getElementById('lightbox-img');

        // Prevent image drag behavior
        gallery.querySelectorAll('img').forEach(img => {
            img.addEventListener('dragstart', e => e.preventDefault());
        });

        // Auto-scroll
        let scrollStep = 260;
        let autoScroll;

        function startAutoScroll() {
            autoScroll = setInterval(() => {
                if ((gallery.scrollLeft + gallery.clientWidth) >= gallery.scrollWidth) {
                    gallery.scrollTo({
                        left: 0,
                        behavior: 'smooth'
                    });
                } else {
                    gallery.scrollBy({
                        left: scrollStep,
                        behavior: 'smooth'
                    });
                }
            }, 3000);
        }

        function stopAutoScroll() {
            clearInterval(autoScroll);
        }

        startAutoScroll();

        ['mousedown', 'touchstart'].forEach(evt =>
            gallery.addEventListener(evt, stopAutoScroll)
        );
        ['mouseup', 'touchend'].forEach(evt =>
            gallery.addEventListener(evt, startAutoScroll)
        );

        // Lightbox
        function openLightbox(src) {
            lightboxImg.src = src;
            lightbox.style.display = 'flex';
            stopAutoScroll();
        }

        function closeLightbox() {
            lightbox.style.display = 'none';
            startAutoScroll();
        }

        // Mouse drag-to-scroll
        let isDown = false;
        let startX;
        let scrollLeft;
        let isDragging = false;

        gallery.addEventListener('mousedown', (e) => {
            isDown = true;
            startX = e.pageX - gallery.offsetLeft;
            scrollLeft = gallery.scrollLeft;
            isDragging = false;
        });

        gallery.addEventListener('mouseleave', () => {
            isDown = false;
        });

        gallery.addEventListener('mouseup', () => {
            isDown = false;
            setTimeout(() => isDragging = false, 0); // Reset
        });

        gallery.addEventListener('mousemove', (e) => {
            if (!isDown) return;
            e.preventDefault();
            const x = e.pageX - gallery.offsetLeft;
            const walk = (x - startX) * 1.5;
            if (Math.abs(walk) > 5) isDragging = true;
            gallery.scrollLeft = scrollLeft - walk;
        });

        // Prevent lightbox open on accidental drag-click
        gallery.querySelectorAll('img').forEach(img => {
            img.addEventListener('click', function(e) {
                if (!isDragging) openLightbox(this.src);
            });
        });
    </script>

